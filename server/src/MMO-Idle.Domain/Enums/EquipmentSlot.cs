namespace MMOIdle.Domain.Enums;

/// <summary>
/// 表示游戏中各种装备可以放置的槽位
/// </summary>
public enum EquipmentSlot
{
    /// <summary>
    /// 头部装备槽位，用于放置如头盔等头部装备
    /// </summary>
    Head = 1 ,
    /// <summary>
    /// 肩部装备槽位，可放置肩部的护具等装备
    /// </summary>
    Shoulder,
    /// <summary>
    /// 胸部装备槽位，用于放置胸甲之类的胸部防护装备
    /// </summary>
    Chest,
    /// <summary>
    /// 背部装备槽位，能放置披风、背包等背部相关装备
    /// </summary>
    Back,
    /// <summary>
    /// 腕部装备类型，如护腕
    /// </summary>
    Wrist,
    /// <summary>
    /// 腰部装备槽位，可放置腰带等腰部装备
    /// </summary>
    Waist,
    /// <summary>
    /// 手部装备槽位，可以装备手套等手部用品
    /// </summary>
    Hands,
    /// <summary>
    /// 主手装备槽位，通常放置主要使用的武器
    /// </summary>
    MainHand,
    /// <summary>
    /// 腿部装备槽位，用于放置护腿等腿部防护装备
    /// </summary>
    Legs,
    /// <summary>
    /// 副手装备槽位，可放置盾牌、副手武器或其他辅助工具
    /// </summary>
    OffHand,
    /// <summary>
    /// 脚部装备槽位，能装备靴子等脚部用品
    /// </summary>
    Feet,
    /// <summary>
    /// 项链装备槽位，用于佩戴项链等饰品
    /// </summary>
    Neck,
    /// <summary>
    /// 戒指1装备槽位，可佩戴一枚戒指
    /// </summary>
    Ring1,
    /// <summary>
    /// 戒指2装备槽位，可佩戴另一枚戒指
    /// </summary>
    Ring2,
    /// <summary>
    /// 饰品1装备槽位，能放置如耳环、徽章等饰品
    /// </summary>
    Accessory1,
    /// <summary>
    /// 饰品2装备槽位，可放置另一个饰品
    /// </summary>
    Accessory2,
    /// <summary>
    /// 镐子装备槽位，用于放置镐子工具
    /// </summary>
    Pickaxe,
    /// <summary>
    /// 小刀装备槽位，可放置小刀类工具
    /// </summary>
    Knife,
    /// <summary>
    /// 镰刀装备槽位，用于放置镰刀工具
    /// </summary>
    Sickle,
    /// <summary>
    /// 鱼竿装备槽位，可放置钓鱼用的鱼竿
    /// </summary>
    FishingRod,
    /// <summary>
    /// 锄头装备槽位，用于放置锄头农具
    /// </summary>
    Hoe,
    /// <summary>
    /// 锤子装备槽位，可放置锤子工具
    /// </summary>
    Hammer,
    /// <summary>
    /// 凿子装备槽位，用于放置凿子工具
    /// </summary>
    Chisel,
    /// <summary>
    /// 针线装备槽位，可放置针线类物品用于缝纫等操作
    /// </summary>
    NeedleAndThread,
    /// <summary>
    /// 铲子装备槽位，用于放置铲子工具
    /// </summary>
    Shovel,
    /// <summary>
    /// 锅具装备槽位，可放置烹饪用的锅具
    /// </summary>
    Cookware
}